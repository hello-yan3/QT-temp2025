void MainWindow::connectInit()
{
    //页面切换响应
    connect(ui->labClickable_FrontPage,&ClickableLabel::myClicked,this,&MainWindow::onTabClicked);
    connect(ui->lab_icon1,&ClickableLabel::myClicked,this,&MainWindow::onTabClicked);
    connect(ui->labClickable_SettingPage,&ClickableLabel::myClicked,this,&MainWindow::onTabClicked);
    connect(ui->lab_icon2,&ClickableLabel::myClicked,this,&MainWindow::onTabClicked);

    //串口开启响应
    connect(frontPage->btnPush_Turn,&QPushButton::clicked,this,&MainWindow::openClicked);
}

/* ******************************************************************************
 * @name     switchPage
 * @date     2022-03-22
 * @brief    页面切换实现函数，处理UI变换
 * @author   Elon Joker
 * @param    index 页面下标，从0开始
 * @return   none
 * *****************************************************************************/
void MainWindow::switchPage(int index)
{
    QString style[2] ={"background-color:rgb(52,134,218);","background-color:transparent;"};
    int count = ui->stackedWidget->count();
    if(index >= count)
    {
        index = 0;
    }
    ui->wid_FrontPage->setStyleSheet(style[index==0?0:1]);
    ui->wid_SettingPage->setStyleSheet(style[index==1?0:1]);
    ui->stackedWidget->setCurrentIndex(index);
}

/* ******************************************************************************
 * @name     onTabClicked
 * @date     2022-03-22
 * @brief    页面切换
 * @author   Elon Joker
 * @param    none
 * @return   none
 * *****************************************************************************/
void MainWindow::onTabClicked()
{
    ClickableLabel *pLabel = qobject_cast<ClickableLabel *>(sender());
    //首页
    if(pLabel==ui->labClickable_FrontPage||pLabel==ui->lab_icon1)
    {
        switchPage(0);
    }
    //设置
    if(pLabel==ui->labClickable_SettingPage||pLabel==ui->lab_icon2)
    {
        switchPage(1);
    }
}

/* ******************************************************************************
 * @name     openClicked
 * @date     2022-03-23
 * @brief    开启通信的槽函数，用于建立串口通信
 * @author   Elon Joker
 * @param    none
 * @return   none
 * *****************************************************************************/
void MainWindow::openClicked()
{
    if("开启通信"==frontPage->btnPush_Turn->text())
    {
        if("NO"==settingPage->getPortName())  // 如果没有配置串口
        {
            //提示检查串口配置
            WarningWindow *warningWindow = new  WarningWindow();
            warningWindow->setTitleStyle("提示",":/hint.png");
            warningWindow->showWarningMsg("请检查串口配置！");
            warningWindow->setWindowModality(Qt::ApplicationModal);//屏蔽其他窗口
            warningWindow->show();
        }
        else
        {
            if(settingPage->openSerial())
            {
                 frontPage->btnPush_Turn->setText("关闭通信");
            }
            else  // 串口打开失败
            {
                //提示检查串口配置
                WarningWindow *warningWindow = new  WarningWindow();
                warningWindow->setTitleStyle("错误",":/warning-on.png");
                warningWindow->showWarningMsg("串口开启失败！请检查串口设备！");
                warningWindow->setWindowModality(Qt::ApplicationModal);//屏蔽其他窗口
                warningWindow->show();
            }

        }
    }
    else//关闭串口，
    {
        settingPage->getSerial()->close();
        if(FrontPage::isSave)
            frontPage->btnPush_Save->click();
        frontPage->btnPush_Turn->setText("开启通信");
        //重置
        FrontPage::lastSequence[0]=-1;
        FrontPage::lastSequence[1]=-1;
    }
}







